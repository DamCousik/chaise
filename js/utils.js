Array.prototype.contains = function (elem) {
	for (i in this) {
		if (this[i] == elem) return true;
	}
	return false;
};

Array.prototype.empty = function () {
	this.splice(0, this.length);
};

function emptyJSON(obj) {
	$.each(obj, function(key, value) {
		delete obj[key];
	});
}

function initLocation() {
	HOME = window.location.protocol + '//' + window.location.host;
}

function clearFacets(narrow, box, facets, facebaseData, metadata, colsDescr, colsGroup, colsDefs,
		filterOptions, pagingOptions, sortInfo, chooseColumns, facetClass, score) {
	emptyJSON(narrow);
	emptyJSON(box);
	facets.empty();
	facebaseData.empty();
	//alert(JSON.stringify(metadata, null, 4));
	emptyJSON(metadata);
	emptyJSON(colsDescr);
	emptyJSON(colsGroup);
	colsDefs.empty();
	$.each(filterOptions, function(key, value) {
		if (key == 'filterText') {
			filterOptions[key] = '';
		} else if (key == 'useExternalFilter') {
			filterOptions[key] = true;
		} else {
			delete filterOptions[key];
		}
	});
	$.each(pagingOptions, function(key, value) {
		if (key == 'pageSizes') {
			value.empty();
			value.push(25);
			value.push(50);
			value.push(100);
		} else if (key == 'pageSize') {
			pagingOptions[key] = 25;
		} else if (key == 'currentPage') {
			pagingOptions[key] = 1;
		} else {
			delete pagingOptions[key];
		}
	});
	$.each(sortInfo, function(key, value) {
		if (key == 'fields') {
			value.empty();
		} else if (key == 'directions') {
			value.empty();
		} else {
			delete sortInfo[key];
		}
	});
	emptyJSON(chooseColumns);
	emptyJSON(facetClass);
	score.empty();
}
